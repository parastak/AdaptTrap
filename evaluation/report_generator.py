
# evaluation/report_generator.py
"""
Assembles evaluation/final_report.md from the two eval runs + the training
log. Every number in the report is computed here, at generation time, from
the raw JSON files -- nothing is hardcoded or pasted in from a chat/console
summary. Re-run this after any new eval run and the report updates itself.
 
Usage:
    python -m evaluation.report_generator \
        --run1 logs/eval_results_run1.json \
        --run2 logs/eval_results_run2.json \
        --training-log logs/training2.json \
        --out evaluation/final_report.md \
        --plot-dir logs/plots
"""
 
from __future__ import annotations
 
import argparse
from pathlib import Path
 
from evaluation.plots import generate_all_plots
from evaluation.stats_utils import (
    ATTACKER_ORDER,
    DEFENDER_ORDER,
    format_rate_with_ci,
    load_run,
    pooled_episode_level_counts,
    pooled_session_level_counts,
    recompute_cross_seed_table,
)
 
NORMALIZED_REWARD_CAVEAT = """
> **Known artifact -- `normalized_reward` is not a comparison metric.**
> `mean_total_normalized_reward` in the raw eval JSONs is a per-run,
> self-referential rolling z-score: `HoneypotEnv` builds a fresh reward
> history per (defender, attacker) run and normalizes each step against
> *that run's own* mean/std. Because a consistently strong policy sits close
> to its own running mean, its normalized score compresses toward zero even
> though its raw reward is higher -- this is why `rl_greedy` shows a *lower*
> normalized reward than `static` despite winning on every raw metric. This
> is a reporting-layer artifact, not a policy deficiency. **`raw_reward` and
> `detection_rate` are the metrics this report treats as ground truth.**
"""
 
SCOPE_NOTE = """
## Scope and limitations
 
- Single simulated environment (SSH/HTTP/Redis emulated services), not a
  live network target.
- Three attacker tiers, two of which (`recon_probe`, `scripted_exploit`)
  are scripted/heuristic rather than adaptive; only `ai_probe` learns within
  a session (fresh Q-learning bandit instance per session -- see
  `attackers/level3_bandit.py`).
- No attacker in this benchmark adapts *to the trained RL policy itself*
  across sessions or episodes -- each attacker session starts from a fresh
  instance with no memory of prior defender behavior. The results show the
  defender beating fixed/heuristic adversaries, not an arms-race outcome
  against an adversary that is itself learning to counter the RL policy.
- Evaluated on CPU, `fast_training=True` timing scale, 2 random seeds
  (42, 123), 50 episodes per attacker per seed.
"""
 
 
def _fmt_pct(x: float | None) -> str:
    return "n/a" if x is None else f"{x:+.2f}%"
 
 
def build_report(
    run1_path: str,
    run2_path: str,
    training_log_path: str,
    plot_dir: str,
    out_path: str,
) -> Path:
    run_paths = [run1_path, run2_path]
    data1 = load_run(run1_path)
    data2 = load_run(run2_path)
 
    cfg1, cfg2 = data1["config"], data2["config"]
    seeds = [cfg1["seed"], cfg2["seed"]]
    episodes_per_attacker = cfg1["episodes_per_attacker"]
    checkpoint = cfg1["checkpoint_path"]
 
    assert cfg1["defenders"] == cfg2["defenders"], "defender lists differ between runs"
    assert cfg1["attackers"] == cfg2["attackers"], "attacker lists differ between runs"
 
    plots = generate_all_plots(run_paths, training_log_path, out_dir=plot_dir)
    plot_dir_rel = Path(plot_dir)
 
    # -- cross-seed reward deltas, recomputed from JSON (not trusted) --------
    delta_table = recompute_cross_seed_table(run_paths)
 
    # -- overall scoreboards for both runs -----------------------------------
    def overall_row(data, defender):
        o = data["scoreboard"][defender]["overall"]
        return o.get("mean_total_raw_reward", 0.0), o.get("detection_rate_steps", 0.0)
 
    lines: list[str] = []
    lines.append("# AdaptTrap -- Evaluation Report\n")
    lines.append(
        f"*Auto-generated by `evaluation/report_generator.py`. "
        f"Checkpoint: `{checkpoint}`. Seeds: {seeds}.*\n"
    )
 
    # ---------------- Setup ----------------
    lines.append("## Setup\n")
    lines.append(
        f"- Attacker tiers: {', '.join(ATTACKER_ORDER)}\n"
        f"- Defenders compared: {', '.join(DEFENDER_ORDER)}\n"
        f"- Episodes per attacker per run: {episodes_per_attacker} "
        f"({episodes_per_attacker * len(ATTACKER_ORDER)} episodes/defender/run)\n"
        f"- Independent seeds: {seeds[0]} and {seeds[1]}\n"
        f"- Checkpoint evaluated: `{checkpoint}` (same checkpoint, both runs)\n"
    )
 
    # ---------------- Headline ----------------
    lines.append("\n## Headline result\n")
    lines.append(
        "RL defender beats every non-RL baseline on raw reward, in both "
        "independent seed runs, on every attacker tier:\n"
    )
    lines.append("| Attacker | Seed | Baseline (static) reward | RL reward | Delta |")
    lines.append("|---|---|---|---|---|")
    for attacker in ATTACKER_ORDER:
        for row in delta_table[attacker]["per_run"]:
            lines.append(
                f"| {attacker} | {row['seed']} | {row['baseline']} | {row['rl']} "
                f"| {_fmt_pct(row['pct_delta'])} |"
            )
    lines.append("")
 
    lines.append(f"![Reward by attacker]({plot_dir_rel / 'reward_by_attacker.png'})\n")
 
    # ---------------- Detection rate + CI ----------------
    lines.append("### Detection rate against `ai_probe` (hardest tier)\n")
    lines.append(
        "Zero observed detections is not the same claim as a zero true "
        "detection rate. Two units of analysis are reported below. "
        "**Episode-level is the primary, defensible claim** -- within a "
        "single episode the defender observes `session_count` and "
        "`consec_detects` (see `HoneypotEnv._get_obs()`), so its action in "
        "session *t* can depend on the outcome of sessions *1..t-1*. That "
        "breaks strict session-to-session independence on the defender "
        "side, even though each attacker instance itself is freshly built "
        "with no carried state (`HoneypotEnv._build_attacker()`):\n"
    )

    d_ep, n_ep = pooled_episode_level_counts(run_paths, "rl_greedy", "ai_probe")
    d_sess, n_sess = pooled_session_level_counts(run_paths, "rl_greedy", "ai_probe")

    lines.append(
        f"- **Episode-level (primary claim)** -- one independent trial per "
        f"full 20-session episode; counts as a detection if any session "
        f"inside it was flagged: {format_rate_with_ci(d_ep, n_ep)}\n"
    )
    lines.append(
        f"- **Session-level (descriptive telemetry only -- not used for "
        f"the confidence interval)**: {d_sess} of {n_sess} individual "
        f"sessions flagged. Reported as a raw count, not as an "
        f"independent-trial rate, because the defender-side dependency "
        f"described above applies at this unit of analysis.\n"
    )

    lines.append("Same comparison for the non-RL baselines against `ai_probe`, "
                  "pooled across both seeds (episode-level, the primary unit):\n")
    lines.append("| Defender | Episodes with >=1 detection | Episode-level rate (95% CI) |")
    lines.append("|---|---|---|")
    for defender in DEFENDER_ORDER:
        d, n = pooled_episode_level_counts(run_paths, defender, "ai_probe")
        lines.append(f"| {defender} | {d} / {n} | {format_rate_with_ci(d, n)} |")
    lines.append("")

    lines.append(f"![Detection rate by attacker]({plot_dir_rel / 'detection_rate_by_attacker.png'})\n")
    lines.append(f"![Dwell time by attacker]({plot_dir_rel / 'dwell_time_by_attacker.png'})\n")

    
    # ---------------- Training curve ----------------
    lines.append("## Training curve\n")
    lines.append(
        "1500-episode training run, curriculum phases marked. See "
        "`logs/training2.json` for the full per-episode log.\n"
    )
    lines.append(f"![Training reward curve]({plot_dir_rel / 'training_reward_curve.png'})\n")
 
    # ---------------- normalized_reward caveat ----------------
    lines.append(NORMALIZED_REWARD_CAVEAT)
 
    # ---------------- Scope ----------------
    lines.append(SCOPE_NOTE)
 
    out = Path(out_path)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text("\n".join(lines), encoding="utf-8")
    return out
 
 
def main() -> None:
    parser = argparse.ArgumentParser(description="Build the AdaptTrap final evaluation report.")
    parser.add_argument("--run1", default="logs/eval_results_run1.json")
    parser.add_argument("--run2", default="logs/eval_results_run2.json")
    parser.add_argument("--training-log", default="logs/training2.json")
    parser.add_argument("--plot-dir", default="logs/plots")
    parser.add_argument("--out", default="evaluation/final_report.md")
    args = parser.parse_args()
 
    out_path = build_report(
        run1_path=args.run1,
        run2_path=args.run2,
        training_log_path=args.training_log,
        plot_dir=args.plot_dir,
        out_path=args.out,
    )
    print(f"[report_generator] Wrote report to {out_path}")
 
 
if __name__ == "__main__":
    main()
 